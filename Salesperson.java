public class Salesperson {
    private int salespersonID;
    private String name;
    private String phone;
    private String email;

    public Salesperson(int salespersonID, String name, String phone, String email) {
        this.salespersonID = salespersonID;
        this.name = name;
        this.phone = phone;
        this.email = email;
    }
}